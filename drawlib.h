void draw_text(const unsigned char *string, int x, int y, int color){
    int i;
    for(i = 0; string[i] != 0; i++){
        draw_letter(string[i], x+(i*8), y, color);
    }
}

void draw_vertical_text(const unsigned char *string, int x, int y, int color){
    int i, space;
    for(i = 0; string[i] != 0; i++){
        if(i > 0 && string[i-1] == 'w')
            space = 9;
        draw_vertical_letter(string[i], x, y+(i*space), color);
        space = 8;
    }
}
